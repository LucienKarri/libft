#include "libft.h"

int		ft_atoi(char *str)
{
	int		i;
	int		znak;
	long	num;
	long	output;

	i = 0;
	output = 0;
	znak = 1;
	while (str[i] == ' ' || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '-')
	{
		znak = -1;
		i++;
	}
	else if (str[i] == '+')
		i++;
	while (str[i] >= '0' && str[i] <= '9')
	{
		num = str[i] - '0';
		output = output * 10 + num;
		i++;
	}
	return (output * znak);
}
